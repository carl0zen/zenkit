/**
 * ZenKit Benchmark Report Generator
 *
 * Reads a benchmark result JSON and produces a human-readable markdown report.
 *
 * Usage: npx tsx benchmark/scripts/report.ts [result-path]
 */
import fs from 'fs'
import path from 'path'

function main() {
  const resultPath = process.argv[2] || 'benchmark/results/schema-validator-playground.json'
  const resolved = path.resolve(resultPath)

  if (!fs.existsSync(resolved)) {
    console.error(`Result file not found: ${resolved}`)
    process.exit(1)
  }

  const result = JSON.parse(fs.readFileSync(resolved, 'utf-8'))

  const duration = result.duration_ms
  const minutes = Math.floor(duration / 60000)
  const seconds = Math.floor((duration % 60000) / 1000)

  const lines: string[] = [
    `# Benchmark Report: ${result.task_name}`,
    '',
    `**Benchmark ID:** ${result.benchmark_id}`,
    `**Status:** ${result.status.toUpperCase()}`,
    `**Duration:** ${minutes}m ${seconds}s`,
    `**Date:** ${result.start_time}`,
    '',
    '## Summary',
    '',
    `| Metric | Value |`,
    `|--------|-------|`,
    `| Tests Run | ${result.validation.tests_run} |`,
    `| Tests Passed | ${result.validation.tests_passed} |`,
    `| Tests Failed | ${result.validation.tests_failed} |`,
    `| Schema Valid | ${result.validation.schema_valid ? 'Yes' : 'No'} |`,
    `| Retries | ${result.retries} |`,
    '',
    '## Telemetry',
    '',
    `| Metric | Value | Source |`,
    `|--------|-------|--------|`,
    `| Tokens | ~${result.telemetry.estimated_tokens?.toLocaleString() || 'N/A'} | ${result.telemetry.telemetry_source} |`,
    `| Cost | ~$${result.telemetry.estimated_cost_usd?.toFixed(2) || 'N/A'} | ${result.telemetry.telemetry_source} |`,
    '',
    '## Stages',
    '',
  ]

  for (const stage of result.stages) {
    const icon = stage.status === 'pass' ? 'PASS' : stage.status === 'fail' ? 'FAIL' : 'SKIP'
    const dur = stage.duration_ms ? `${(stage.duration_ms / 1000).toFixed(1)}s` : '—'
    lines.push(`- **[${icon}]** ${stage.name} (${dur}) — ${stage.notes}`)
  }

  if (result.files_changed?.length > 0) {
    lines.push('', '## Files Changed', '')
    for (const file of result.files_changed) {
      lines.push(`- \`${file}\``)
    }
  }

  if (result.uncertainty_notes?.length > 0) {
    lines.push('', '## Uncertainty Notes', '')
    for (const note of result.uncertainty_notes) {
      lines.push(`- ${note}`)
    }
  }

  lines.push('', '---', '', '*Generated by ZenKit Benchmark Reporter*')

  const report = lines.join('\n')

  // Write markdown report alongside the JSON
  const mdPath = resolved.replace(/\.json$/, '.md')
  fs.writeFileSync(mdPath, report)

  console.log(report)
  console.log(`\nReport written to: ${mdPath}`)
}

main()
